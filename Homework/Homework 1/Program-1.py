# WAP To Find The Sum of All Elements In A List

lst = [1, 2, 3, 4, 5]
sum_of_elements = sum(lst)

print("The Sum of All Elements : ", sum_of_elements)

# Taking User Input (Eval)

lst = eval(input("Enter A List Of Numbers : "))
sum_of_elements = sum(lst)

print("The Sum of All Elements : ", sum_of_elements)
